// 🔹 What is concat()?

// 👉 concat() is used to merge (join) two or more arrays
// 👉 It returns a new array (does NOT change the original arrays 🔥)


let firstArr = ["red", "yellow", "blue"];
let secondArr = ["orange", "green", "violet"];
let result = firstArr.concat(secondArr);
console.log(firstArr);
console.log(secondArr);
console.log(result);


// revser Methode ;
result.reverse();
console.log(result);

